export type TranslationKey = keyof typeof translations.en;

export const t = (key: string, lang: 'en' | 'ms' = 'en'): string => {
  const keys = key.split('.');
  let value: any = translations[lang];

  for (const k of keys) {
    value = value[k];
    if (value === undefined) return key;
  }

  return value;
};
